	<h2>Walking a Section</h2>
	
	<p>Section legs and frames are both light and rigid enough to &quot;walk&quot; out into position. This can be done by one person without having to get in the water.</p>
			<ul> 
				
				<li><span class="blue">Step 1:</span> Stand on edge of shore facing lake.
				</li>
				
				
				<li><span class="blue">Step 2:</span> Attach legs to aluminum frame at an approximate depth.
				</li>
			
				
				<li><span class="blue">Step 3:</span> Place the end of the section and legs into the water.
				</li>
			
				
				<li><span class="blue">Step 4:</span> Grip Frame and tilt section sideways onto one leg. Pivot Section on Leg, advancing the other Leg deeper. Keep walking Section side to side until it is in position. Place on shore or attach Hook-Plates into Hook-Plate Slots on previous Section. Level Section from side-to-side. If Section needs height adjustment use a Winch-Jack, or pull the section back, adjust accordingly, and walk out into place again.
				</li>
				
				
				<li><span class="blue">Step 5:</span> Place three pallets of decking on the Section, starting at the shore and working out. As each Section is added, carry Frames out to the end and install Legs before walking into position, leveling and installing Decking.
				</li>
		
			</ul> 

			<p class="tinytext"><a href="development/walking_your_dock.pdf" target="blank">Print pdf version</a></p>

			
			<h2>Installation Tips</h2>
	
	<p><strong>Tools needed:</strong><br>Two <sup>9</sup>&frasl;<sub>16</sub>" wrenches or deep well socket<br>One &frac12;" wrench<br>Level</p>
			<ul> 
				<li><span class="blue">Cross-Braces</span> (optional): One or two may be needed on sections where water depth is over 3'. Prior to installing the section, attach bottom of the Cross-Brace(s) to the Leg with Small U-Bolt using &frac12;" wrench. Leave the top unbolted until the section is adjusted and leveled to prevent binding.
				</li>

				<li><span class="blue">Ladder/Bench:</span> Can be attached anywhere on the long (2" x 4" x 10') side of a section or wing. Lift Decking and place Large U-Bolts around Frame with threads pointing out. U-Bolt fits in between Decking Boards. Place accessory on U-Bolts through the Holes and tighten Nuts with <sup>9</sup>&frasl;<sub>16</sub>" wrench.
				</li>

				<li><span class="blue">Dock Height:</span> Suggested height is 18" from water's surface to the bottom of frame. This height should protect the dock from large waves. Dock may need adjustment if water level changes. 
				</li>

				<li><span class="blue">1st Section:</span> Place two Legs into Leg-Guides with pads pointing out. Fit Large U-Bolt around leg, through Leg-Guide and Frame with threads pointing in. Attach Flat-Washers and Nuts with a <sup>9</sup>&frasl;<sub>16</sub>" wrench. You may need to angle Section up or down to meet the desired height off water.<br>
				<strong>Important:</strong> Sections must be leveled side-to-side by place level on 2" x 3" rectangular tube. When section is level tighten U-Bolts. Place Decking on Section starting at shore end. First pallet (Six-Long) has 6 Boards with two stringers that stick out on each end. Second pallet (Seven) has 7 boards. The last pallet (Six-Short) has six boards and two stringers that stick out on one end and are recessed on the other. Place the recessed side at the end of the Section against the 2" x 3" rectangular tube.
				</li>

				<li><span class="blue">2nd Section:</span> The Hook-Plates located at the shore end of this Section fit into the Hook-Plate Slots on the previous Section. Angle the second Section toward the desired height off the water. Level from side-to-side. Placement of pallets is the same as first Section.
				</li>

				<li><span class="blue">3rd Section:</span> Repeat steps for section 2.
				</li>

				<li><span class="blue">Wings:</span> Same installation concept as a regular Section. Attach to dock with Wing-Plates that rest on top of dock Frame between Decking Boards. Weight of Wing and decking are more than enough to keep in place.
				</li>
		</ul> 
		
	
